package Main;

/**
 * Author
 */
public class Author {
    private String name;
    private int age;

    // set
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    // Get
    public String getName() {
        return this.name;
    }

    public int getAge() {
        return this.age;
    }
}